    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        ClassSelectDialog selector = null;
        if (transmutor instanceof Refiner) {
            selector = new ClassSelectDialog(null, true, DataHandler.getURLFilters());
        } else {
            selector = new ClassSelectDialog(null, true, DataHandler.getVisitFilters());
        }
        selector.show();
        Object result = selector.getSelection();
        if (result != null && !selector.isCancelled()) {
            if (transmutor instanceof Refiner) {
                ((Refiner) transmutor).addPrefilter((URLFilter) result);
            } else {
                ((Extractor) transmutor).addPrefilter((VisitFilter) result);
            }
            DefaultListModel lm = (DefaultListModel) filterList.getModel();
            lm.addElement(result);
        }
    }//GEN-LAST:event_addButtonActionPerformed

