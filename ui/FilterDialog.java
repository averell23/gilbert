    /**
     * Intializes this form.
     */
    private void initForm() {
        DefaultListModel listModel = new DefaultListModel();
        filterList.setModel(listModel);
        Vector filters = null;
        if (transmutor instanceof Refiner) {
            filters = ((Refiner) transmutor).getPrefilters();
        } else {
            filters = ((Extractor) transmutor).getPrefilters();
        }
        Enumeration filterE = filters.elements();
        
        while (filterE.hasMoreElements()) {
            listModel.addElement(filterE.nextElement());
        }
    }
    

